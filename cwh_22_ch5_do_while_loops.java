public class cwh_22_ch5_do_while_loops {
    public static void main(String[] args) {
    int a = 1;
    do {
       System.out.println(a);
        a++;
    } while (a < 5) ;
    int b = 1;
    do {
        System.out.println(String.valueOf(b));
        b++;
    }while (b < 10000);


    }

}

